// The Northwind Manufacturing seeded demo data. This is the source of truth
// for the V15 Enterprise Demo. The data is real (a believable mid-sized
// contract manufacturer), realistic (working projects, active sales, active
// operations), and rich (~200 pieces of knowledge, ~30 pending approvals,
// 5 active AI tool connections, a 30-day history of changes).
//
// The data is exported as a typed TypeScript module. The marketing site is
// static (output: "export"), so the data is the build-time source of truth.
// The demo views read from this data; the "Reset demo" button in the UI
// triggers a page reload that re-runs the seed.

// ---------- Types ----------

export type Department =
  | "Executive"
  | "HR"
  | "Sales"
  | "Operations"
  | "Finance"
  | "IT"
  | "Engineering";

export type KnowledgeCategory =
  | "policy"
  | "procedure"
  | "decision"
  | "approved_change"
  | "rejected_proposal"
  | "historical"
  | "ai_proposal"
  | "ai_recall"
  | "manager_review"
  | "correction"
  | "conflict"
  | "merge_history"
  | "audit";

export type KnowledgeStatus = "trusted" | "pending_review" | "retracted";

export type Person = {
  id: string;
  name: string;
  role: "owner" | "admin" | "member";
  department: Department;
  title: string;
  email: string;
};

export type Project = {
  id: string;
  name: string;
  description: string;
  status: "active" | "completed" | "paused";
  owner: string;
};

export type Knowledge = {
  id: string;
  category: KnowledgeCategory;
  title: string;
  body: string;
  project?: string;
  department: Department;
  author: string; // person id or ai tool name
  approver?: string; // person id
  status: KnowledgeStatus;
  trust: number; // 0-1
  createdAt: string; // ISO date
  approvedAt?: string;
  retractedAt?: string;
  recalledBy?: string; // for ai_recall
  supersedes?: string; // for supersede relationships
  retractedReason?: string;
  corrections?: string[];
};

export type Connection = {
  id: string;
  name: "Claude" | "Codex" | "Copilot" | "Gemini" | "Internal Automation";
  identity: string; // e.g., "ai:claude-code-2"
  connectedAt: string;
  lastActivity: string;
  proposed: number;
  recalled: number;
};

export type Activity = {
  id: string;
  type:
    | "proposed"
    | "approved"
    | "retracted"
    | "recalled"
    | "corrected"
    | "merged"
    | "connected"
    | "invited";
  knowledge?: string; // knowledge id
  person?: string; // person id or ai tool name
  at: string;
  summary: string;
};

